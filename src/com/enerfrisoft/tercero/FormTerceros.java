    private void buildAutoCompleterNUMIDTERCERO() {
        acNUMIDTERCERO = new TextAutoCompleter(NUMIDTERCERO, new AutoCompleterCallback() {
            @Override
            public void callback(Object o) {
                updateTercero = true;

//                if (!TIPOIDTERCERO.getSelectedItem().toString().equals("NIT")) {
//                    addSede.setEnabled(false);
//                }
                //campo expedicion
                String expedicion = UniversalDAO.getString(""
                        + "select municipio.nombre "
                        + "from municipio,tercero "
                        + "where "
                        + "tercero.NUMIDTERCERO = '" + o.toString() + "' and "
                        + "municipio.id = tercero.EXPEDICION", conn);
                EXPEDICION.setText(expedicion);

                //campo nombre
                String nombre = UniversalDAO.getString(""
                        + "select coalesce(NOMIDTERCERO,'') "
                        + "from tercero "
                        + "where tercero.NUMIDTERCERO='" + o.toString() + "'", conn);
                NOMIDTERCERO.setText(nombre);

                //campo primer apellido
                String primerApellido = UniversalDAO.getString(""
                        + "select coalesce(PRIMERAPELLIDOIDTERCERO,'') "
                        + "from tercero "
                        + "where tercero.NUMIDTERCERO='" + o.toString() + "'", conn);
                PRIMERAPELLIDOIDTERCERO.setText(primerApellido);

                //campo segundo apellido
                String segundoApellido = UniversalDAO.getString(""
                        + "select coalesce(SEGUNDOAPELLIDOIDTERCERO,'') "
                        + "from tercero "
                        + "where tercero.NUMIDTERCERO='" + o.toString() + "'", conn);
                SEGUNDOAPELLIDOIDTERCERO.setText(segundoApellido);

                //email
                String email = UniversalDAO.getString(""
                        + "select coalesce(EMAIL,'') "
                        + "from tercero "
                        + "where tercero.NUMIDTERCERO='" + o.toString() + "'", conn);
                EMAIL.setText(email);

                //Categoria licencia
                String catLicencia = UniversalDAO.getString(""
                        + "select coalesce(CODCATEGORIALICENCIACONDUCCION,'') "
                        + "from tercero "
                        + "where tercero.NUMIDTERCERO='" + o.toString() + "'", conn);
                CODCATEGORIALICENCIACONDUCCION.setSelectedItem(catLicencia);

                //Numero de licencia
                String numLicencia = UniversalDAO.getString(""
                        + "select coalesce(NUMLICENCIACONDUCCION,'') "
                        + "from tercero "
                        + "where tercero.NUMIDTERCERO='" + o.toString() + "'", conn);
                NUMLICENCIACONDUCCION.setText(numLicencia);

                //Fecha de vencimiento licencia
                String sFECHAVENCIMIENTOLICENCIA = UniversalDAO.getString(""
                        + "select coalesce(FECHAVENCIMIENTOLICENCIA,'') "
                        + "from tercero "
                        + "where tercero.NUMIDTERCERO='" + o.toString() + "'", conn);
                try {
                    Date date = new SimpleDateFormat("yyyy-MM-dd").parse(sFECHAVENCIMIENTOLICENCIA);
                    FECHAVENCIMIENTOLICENCIA.setDate(date);
                } catch (Exception e) {

                    System.out.println("date parsing FECHAVENCIMIENTOLICENCIA error");
                }

                //Sedes del tercero
                ComboBoxModel modelSede = UniversalDAO.getComboBoxModel(""
                        + "select sede.CODSEDETERCERO "
                        + "from sede "
                        + "where sede.NUMIDTERCERO = '" + o.toString() + "'", conn);
                modelCODSEDETERCERO.setModel(modelSede);
                modelCODSEDETERCERO.setSelectedItem(null);
                modelCODSEDETERCERO.setEnabled(true);

                //Info Adicional
                String info = UniversalDAO.getString(""
                        + "select coalesce(infoAdicional,'') "
                        + "from tercero "
                        + "where tercero.NUMIDTERCERO='" + o.toString() + "'", conn);
                infoAdicional.setText(info);
            }
        });
        acNUMIDTERCERO.removeAllItems();
        ArrayList<String> data = UniversalDAO.getArrayList(""
                + "select tercero.NUMIDTERCERO "
                + "from tercero "
                + "where tercero.CODTIPOIDTERCERO='" + CODTIPOIDTERCERO.getText() + "'", conn);
        for (int i = 0; i < data.size(); i++) {
            acNUMIDTERCERO.addItem(data.get(i));
        }

    }

