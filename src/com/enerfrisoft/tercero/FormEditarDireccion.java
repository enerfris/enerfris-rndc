    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (!letras.getText().equals("")) {
            if (!last.equals("letras")) {
                String before = resultante.getText();
                resultante.setText(before + " " + letras.getText());
                last = "letras";
                letras.setText("");
                this.direccion = resultante.getText();
                form.setNOMENCLATURADIRECCION(direccion);
            } else {
                Modal.show("Error", "No puede escribir dos letras seguidas", this, "", "error");
            }
        } else {
            Modal.show("Alerta", "Escriba una letra primero", this, "", "warning");
        }

    }//GEN-LAST:event_jButton5ActionPerformed

    private void letrasKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_letrasKeyPressed
        try {
            if (letras.getText().length() >= 1) {
                letras.setText(letras.getText().substring(0, 0));
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_letrasKeyPressed

    private void letrasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_letrasKeyReleased
        try {
            String cadena = letras.getText().toUpperCase();
            letras.setText(cadena);
            int valorASCII = cadena.charAt(0);
            if (valorASCII != 165 && (valorASCII < 65 || valorASCII > 90)) {
                letras.setText("");
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_letrasKeyReleased

