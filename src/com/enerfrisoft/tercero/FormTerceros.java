    private void registrarTercero() {

        if (CODSEDETERCERO.getText().equals("")) {
            Modal.show("Aviso", "Debe agregar el código de sede del tercero", this, "", "warning");
        } else {
            if (EXPEDICION.getText().equals("") && !NUMLICENCIACONDUCCION.getText().equals("")) {
                Modal.show("Aviso", "Debe ingresar el municipio de expedición del documento del conductor", this, "", "warning");
            } else {
                Thread registro = new Thread(new RegistrarTercero(getValuesTercero(), updateTercero, getValuesSede(), updateSede, this, connectionStatus, NOMIDTERCERO.getText(), dataSource, conn));
                registro.start();
            }
        }
    }

