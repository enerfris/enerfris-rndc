    private void modelCODSEDETERCEROActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modelCODSEDETERCEROActionPerformed
        if (TIPOIDTERCERO.getSelectedItem() != null && modelCODSEDETERCERO.getSelectedItem() != null) {
            CODSEDETERCERO.setEditable(false);
            CODSEDETERCERO.setText(modelCODSEDETERCERO.getSelectedItem().toString());
            String codSedeTercero = modelCODSEDETERCERO.getSelectedItem().toString();

            //Nombre Sede Tercero
            String sNOMSEDETERCERO = UniversalDAO.getString(""
                    + "select coalesce(NOMSEDETERCERO,'') "
                    + "from sede "
                    + "where sede.NUMIDTERCERO = '" + NUMIDTERCERO.getText() + "' and "
                    + "sede.CODSEDETERCERO = '" + codSedeTercero + "'", conn);
            NOMSEDETERCERO.setText(sNOMSEDETERCERO);

            //Telefono
            String sNUMTELEFONOCONTACTO = UniversalDAO.getString(""
                    + "select coalesce(NUMTELEFONOCONTACTO,'') "
                    + "from sede "
                    + "where sede.NUMIDTERCERO = '" + NUMIDTERCERO.getText() + "' and "
                    + "sede.CODSEDETERCERO = '" + codSedeTercero + "'", conn);
            NUMTELEFONOCONTACTO.setText(sNUMTELEFONOCONTACTO);

            //Telefono celular
            String sNUMCELULARPERSONA = UniversalDAO.getString(""
                    + "select coalesce(NUMCELULARPERSONA,'') "
                    + "from sede "
                    + "where sede.NUMIDTERCERO = '" + NUMIDTERCERO.getText() + "' and "
                    + "sede.CODSEDETERCERO = '" + codSedeTercero + "'", conn);
            NUMCELULARPERSONA.setText(sNUMCELULARPERSONA);

            //Nomenclatura direccion
            String sNOMENCLATURADIRECCION = UniversalDAO.getString(""
                    + "select coalesce(NOMENCLATURADIRECCION,'') "
                    + "from sede "
                    + "where sede.NUMIDTERCERO = '" + NUMIDTERCERO.getText() + "' and "
                    + "sede.CODSEDETERCERO = '" + codSedeTercero + "'", conn);
            NOMENCLATURADIRECCION.setText(sNOMENCLATURADIRECCION);

            //Municipio dirección
            String sCODMUNICIPIORNDC = UniversalDAO.getString(""
                    + "select CODMUNICIPIORNDC "
                    + "from sede "
                    + "where sede.NUMIDTERCERO = '" + NUMIDTERCERO.getText() + "' and "
                    + "sede.CODSEDETERCERO = '" + codSedeTercero + "'", conn);
            String nombreMunicipio = UniversalDAO.getString(""
                    + "select municipio.nombre "
                    + "from municipio "
                    + "where municipio.id = '" + sCODMUNICIPIORNDC + "'", conn);
            CODMUNICIPIORNDC.setText(nombreMunicipio);
        }
    }//GEN-LAST:event_modelCODSEDETERCEROActionPerformed

    private void TIPOIDTERCEROActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TIPOIDTERCEROActionPerformed
        if (TIPOIDTERCERO.getSelectedItem() != null) {
            if (TIPOIDTERCERO.getSelectedItem().toString().equals("NIT")) {
                EXPEDICION.setEnabled(false);
                CODCATEGORIALICENCIACONDUCCION.setEnabled(false);
                NUMLICENCIACONDUCCION.setEditable(false);
                FECHAVENCIMIENTOLICENCIA.setEnabled(false);
            }

            //cod tipoid tercero
            String tipoId = UniversalDAO.getString(""
                    + "select tipo_id.cod "
                    + "from tipo_id "
                    + "where tipo_id.nombre = '" + TIPOIDTERCERO.getSelectedItem().toString() + "'", conn);
            CODTIPOIDTERCERO.setText(tipoId);

            buildAutoCompleterNUMIDTERCERO();

            limpiarCaracteristicas(false, false);
            if (TIPOIDTERCERO.getSelectedItem().toString().equals("NIT")) {
                EXPEDICION.setEnabled(false);
            }
        }
    }//GEN-LAST:event_TIPOIDTERCEROActionPerformed

