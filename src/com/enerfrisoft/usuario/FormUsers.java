    private void buildTable() {

        try {
            
            Statement sentencia = defaultServerConn.createStatement();

            ResultSet result = sentencia.executeQuery(""
                    + "select * from usuarios");

            int columns = result.getMetaData().getColumnCount();

            //Table's data
            ArrayList<ArrayList<String>> data = new ArrayList<>();

            int rows = 0;
            for (int i = 0; i < columns; i++) {
                data.add(new ArrayList<>());
                while (result.next()) {
                    data.get(i).add(result.getString(i + 1));
                    if (i == 0) {
                        rows++;
                    }

                }
                result.beforeFirst();
            }

            ArrayList<String> columnNames = new ArrayList<>();

            for (int i = 1; i <= columns; i++) {
                columnNames.add(result.getMetaData().getColumnName(i));
            }

            DefaultTableModel tableModel = new DefaultTableModel() {
                public boolean isCellEditable(int rowIndex, int mColIndex) {
                    return false;
                }
            };

            Vector column;
            for (int i = 0; i < columns; i++) {
                column = new Vector();
                for (int j = 0; j < rows; j++) {
                    if (i == 0 || i == 1) {
                        column.add(Tools.decrypt(data.get(i).get(j), this));
                    } else {
                        column.add(data.get(i).get(j));
                    }
                }
                tableModel.addColumn(columnNames.get(i), column);
            }

            usuarios.setModel(tableModel);
            usuarios.getTableHeader().setReorderingAllowed(false);
//            jScrollPane1.setViewportView(table);

            
            sentencia.close();
            result.close();

        } catch (Exception e) {
            System.out.println(e.getLocalizedMessage());
            System.out.println(e.getStackTrace()[0].getLineNumber());
        }
    }

