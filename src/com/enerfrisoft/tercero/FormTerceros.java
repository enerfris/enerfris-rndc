    private void CLEARDATOSDELICENCIA() {
        if (TIPOIDTERCERO.getSelectedItem() != null) {
            if (!TIPOIDTERCERO.getSelectedItem().toString().equals("NIT")) {
                CODCATEGORIALICENCIACONDUCCION.setSelectedItem(null);
                NUMLICENCIACONDUCCION.setText("");
                FECHAVENCIMIENTOLICENCIA.setDate(null);
                CODCATEGORIALICENCIACONDUCCION.setEnabled(true);
                NUMLICENCIACONDUCCION.setEnabled(true);
                FECHAVENCIMIENTOLICENCIA.setEnabled(true);
            }
        }
    }

    private Sede getValuesSede() {
        Sede sede = new Sede();
        Data.auth(sede, conn);

        try {
            sede.setNUMTELEFONOCONTACTO(NUMTELEFONOCONTACTO.getText());
            sede.setNUMCELULARPERSONA(NUMCELULARPERSONA.getText());
            sede.setNOMENCLATURADIRECCION(NOMENCLATURADIRECCION.getText());
            sede.setCODMUNICIPIORNDC(UniversalDAO.getCODMUNICIPIORNDC(CODMUNICIPIORNDC.getText(), conn));
            sede.setCODSEDETERCERO(CODSEDETERCERO.getText());
            sede.setNOMSEDETERCERO(NOMSEDETERCERO.getText());
            sede.setNUMIDTERCERO(NUMIDTERCERO.getText());
            sede.setUserLog(username);

            return sede;
        } catch (Exception e) {
            Thread report = new Thread(new QueryError(new com.enerfrisoft.error.Error(e)));
            report.start();
        }
        return new Sede();
    }

    private Tercero getValuesTercero() {
        try {
            Tercero tercero = new Tercero();
            Data.auth(tercero, conn);
            tercero.setCODTIPOIDTERCERO(UniversalDAO.getCODTIPOID(Items.getValue(TIPOIDTERCERO), conn));
            tercero.setNUMIDTERCERO(NUMIDTERCERO.getText());
            tercero.setEXPEDICION(UniversalDAO.getString("select id from municipio where municipio.nombre = '" + EXPEDICION.getText() + "'", conn));
            tercero.setNOMIDTERCERO(NOMIDTERCERO.getText());
            tercero.setPRIMERAPELLIDOIDTERCERO(PRIMERAPELLIDOIDTERCERO.getText());
            tercero.setSEGUNDOAPELLIDOIDTERCERO(SEGUNDOAPELLIDOIDTERCERO.getText());
            tercero.setNUMTELEFONOCONTACTO(NUMTELEFONOCONTACTO.getText());
            tercero.setNUMCELULARPERSONA(NUMCELULARPERSONA.getText());
            tercero.setNOMENCLATURADIRECCION(NOMENCLATURADIRECCION.getText());
            tercero.setCODMUNICIPIORNDC(UniversalDAO.getCODMUNICIPIORNDC(CODMUNICIPIORNDC.getText(), conn));
            tercero.setCODSEDETERCERO(CODSEDETERCERO.getText());
            tercero.setNOMSEDETERCERO(NOMSEDETERCERO.getText());
            tercero.setNUMLICENCIACONDUCCION(NUMLICENCIACONDUCCION.getText());
            tercero.setCODCATEGORIALICENCIACONDUCCION(Items.getValue(CODCATEGORIALICENCIACONDUCCION));
            try {
                tercero.setFECHAVENCIMIENTOLICENCIA(FECHAVENCIMIENTOLICENCIA.getDate());
            } catch (Exception e) {
                tercero.setFECHAVENCIMIENTOLICENCIA(null);
            }
            tercero.setEMAIL(EMAIL.getText());
            tercero.setEstado("activo");
            tercero.setUserLog(this.username);
            tercero.setInfoAdicional(infoAdicional.getText());

            return tercero;
        } catch (Exception ex) {
            Thread report = new Thread(new QueryError(new com.enerfrisoft.error.Error(ex)));
            report.start();
            System.out.println(ex.getLocalizedMessage() + ":guardarTerceroActionPerformed");
            return null;
        }
    }

    private void buildAutoCompleterMunicipio() {
        TextAutoCompleter acCODMUNICIPIORNDC = new TextAutoCompleter(CODMUNICIPIORNDC, new AutoCompleterCallback() {
            @Override
            public void callback(Object o) {

            }
        });

        ArrayList<String> municipios = ManifiestoDAO.getMunicipios(conn);
        for (int i = 0; i < municipios.size(); i++) {
            acCODMUNICIPIORNDC.addItem(municipios.get(i));
        }
    }

