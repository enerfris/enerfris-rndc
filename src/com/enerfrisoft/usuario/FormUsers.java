    private Boolean verifyFields() {
        if (!nombreUsuario.getText().equals("")
                && (empresa.getSelectedItem() != null)
                && !idUsuario.getText().equals("")
                && !userName.getText().equals("")) {
            return true;
        }
        return false;
    }

    private void registrarUsuario() {
        Usuario usuario = new Usuario();
        usuario.setNombre(Tools.encrypt(nombreUsuario.getText(), this));
        usuario.setUsername(Tools.encrypt(userName.getText(), this));
        usuario.setEstado("activo");
        usuario.setIdentidad(idUsuario.getText());
        if (tipoUsuario.getSelectedItem() != null) {
            usuario.setTipo_usuario(tipoUsuario.getSelectedItem().toString());
        }
        if (empresa.getSelectedItem() != null) {
            usuario.setServer(UniversalDAO.getString(""
                    + "select id "
                    + "from server "
                    + "where nombre='" + empresa.getSelectedItem().toString() + "'", defaultServerConn));
        }
        if (verifyFields()) {
            Boolean option = Modal.showComfirm("Confirmación de registro", ""
                    + "Confirme los siguientes campos\n\n"
                    + "Usuario: " + Tools.decrypt(usuario.getUsername(), this) + "\n"
                    + "Nombre: " + Tools.decrypt(usuario.getNombre(), this) + "\n"
                    + "Identificación: " + usuario.getIdentidad() + "\n"
                    + "Tipo: " + usuario.getTipo_usuario() + "\n"
                    + "Empresa: " + UniversalDAO.getString(""
                            + "select nombre "
                            + "from server "
                            + "where id=" + usuario.getServer() + "", defaultServerConn) + "\n\n", this, "warning");
            if (option) {
                String pass = Modal.passModal("Contraseña", this);
                if (pass != null) {
                    usuario.setPass(Tools.encrypt(pass, this));
                    Thread registro = new Thread(new QueryUsuario(usuario, this));
                    registro.run();
                    buildTable();
                    clear();
                }
            } else {
                Modal.show("Confirmación de registro", "Por favor verifique los campos", this, "", "warning");
            }

        } else {
            Modal.show("Errror", "Falta información por completar para el registro", this, "", "error");
        }

    }

    private void clear() {
        nombreUsuario.setText("");
        userName.setText("");
        empresa.setSelectedItem(null);
        tipoUsuario.setSelectedItem(null);
        idUsuario.setText("");
    }

