    private void buildAutoCompleterNomenclaturas() {
        acNomenclaturas = new TextAutoCompleter(nomenclaturas, new AutoCompleterCallback() {
            @Override
            public void callback(Object o) {
                jButton1.setEnabled(true);
            }
        });
        ArrayList<String> array = UniversalDAO.getArrayList(""
                + "select nombre "
                + "from nomenclaturaDirecciones", conn);
        for (int i = 0; i < array.size(); i++) {
            acNomenclaturas.addItem(array.get(i));
        }
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!last.equals("numero")) {
            String before = resultante.getText();
            resultante.setText(before + " " + numero.getValue().toString());
            this.last = "numero";
            numero.setValue(1);
            this.direccion = resultante.getText();
            form.setNOMENCLATURADIRECCION(direccion);
        } else {
            Modal.show("Error", "No puede escribir dos numeros seguidos", this, "", "error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!last.equals("simbolo")) {
            if (simbolo.getSelectedItem() != null) {
                String before = resultante.getText();
                resultante.setText(before + " " + simbolo.getSelectedItem().toString());
                this.last = "simbolo";
                simbolo.setSelectedItem(null);
                this.direccion = resultante.getText();
                form.setNOMENCLATURADIRECCION(direccion);
            } else {
                Modal.show("Alerta", "Seleccione un simbolo primero", this, "", "warning");
            }
        } else {
            Modal.show("Error", "No puede escribir dos simbolos seguidos", this, "", "error");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!last.equals("nomenclatura")) {
            String before = resultante.getText();
            resultante.setText(before + " " + nomenclaturas.getText());
            last = "nomenclatura";
            nomenclaturas.setText("");
            this.direccion = resultante.getText();
            form.setNOMENCLATURADIRECCION(direccion);
        } else {
            Modal.show("Error", "No puede escribir dos nomenclaturas seguidas", this, "", "error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

